<?php

namespace App\Http\Controllers\Api\Install;

use App\Http\Controllers\Controller;
use App\Models\ImageServer;
use App\Models\Node;
use App\Services\DeployTokenService;
use App\Services\EncryptionService;
use App\Services\NodeAuthService;
use Illuminate\Http\JsonResponse;
use Illuminate\Http\Request;
use Illuminate\Http\Response;
use Symfony\Component\HttpKernel\Exception\HttpException;

class DeployController extends Controller
{
    public function __construct(
        private readonly DeployTokenService $deployTokens,
        private readonly NodeAuthService $nodeAuth,
        private readonly EncryptionService $encryption,
    ) {}

    public function nodeScript(string $token): Response
    {
        $deploy = $this->requireToken($token, DeployTokenService::PURPOSE_NODE);
        $panelUrl = $this->deployTokens->panelUrl();
        $plain = $token;

        $exports = '';
        if ($deploy->resource instanceof Node) {
            $node = $deploy->resource;
            $pmaDomain = '';
            if (filled($node->phpmyadmin_url)) {
                $pmaHost = parse_url((string) $node->phpmyadmin_url, PHP_URL_HOST);
                if (is_string($pmaHost) && $pmaHost !== '' && filter_var($pmaHost, FILTER_VALIDATE_IP) === false) {
                    $pmaDomain = $pmaHost;
                }
            }
            if ($pmaDomain === '') {
                $host = trim((string) $node->hostname);
                if ($host !== '' && str_contains($host, '.') && filter_var($host, FILTER_VALIDATE_IP) === false) {
                    $pmaDomain = $host;
                }
            }
            if ($pmaDomain !== '') {
                $safe = str_replace(['"', "'", "\n", "\r", '\\'], '', $pmaDomain);
                $exports .= "export GAMEPANEL_PHPMYADMIN_DOMAIN=\"{$safe}\"\n";
                $exports .= "export GAMEPANEL_NODE_FQDN=\"{$safe}\"\n";
            }
        }

        $script = <<<BASH
#!/usr/bin/env bash
# GamePanel Node Auto-Deploy (generated by panel)
set -euo pipefail
export DEBIAN_FRONTEND=noninteractive
PANEL_URL="{$panelUrl}"
DEPLOY_TOKEN="{$plain}"
REPO_URL="\${GAMEPANEL_REPO_URL:-https://github.com/nycon/gameinterface.git}"
SRC_DIR="\${GAMEPANEL_SOURCE_DIR:-/opt/gamepanel-src}"
{$exports}
if [[ "\${EUID:-0}" -ne 0 ]]; then
  echo "Bitte als root ausführen (sudo)." >&2
  exit 1
fi

apt-get update -qq
apt-get install -y -qq git curl ca-certificates >/dev/null

if [[ -d "\${SRC_DIR}/.git" ]]; then
  git -C "\$SRC_DIR" fetch --depth 1 origin main 2>/dev/null || true
  git -C "\$SRC_DIR" reset --hard origin/main 2>/dev/null || true
elif [[ -x "\${SRC_DIR}/install.sh" ]]; then
  true
else
  rm -rf "\$SRC_DIR"
  git clone --depth 1 "\$REPO_URL" "\$SRC_DIR"
fi

cd "\$SRC_DIR"
exec ./install.sh --role node --non-interactive \\
  --panel-url "\$PANEL_URL" \\
  --deploy-token "\$DEPLOY_TOKEN" \\
  --tls-insecure
BASH;

        return response($script, 200, [
            'Content-Type' => 'text/x-shellscript; charset=UTF-8',
            'Cache-Control' => 'no-store',
        ]);
    }

